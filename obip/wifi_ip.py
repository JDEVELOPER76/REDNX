import socket

#obtiene la ip del dispositivo mediante la libreria socket
def obtener_ip():
    """Obtiene la direcci√≥n IP local"""
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
        s.connect(("8.8.8.8", 80))
        ip = s.getsockname()[0]
        s.close()
        return ip
    except Exception as e:
        return f"Error: {e}"



